# Variables can store different types of data: strings, integers, floats, and booleans
name = "John Doe"  # A string value
age = 30           # An integer value
height = 6.1       # A floating-point number (decimal)
is_student = False # A boolean value (True or False)

# Output the values of the variables using print()
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height}")
print(f"Is student? {is_student}")
